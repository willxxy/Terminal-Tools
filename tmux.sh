#### TMUX STUFF ####
# Kill tmux session
tmux kill-session -t session_name

# Create new tmux session with a specified name
tmux new-session -s session_name

# Create arbitrary tmux session
tmux

# attach to previously defined session
tmux attach -t session_name

# Note + is combination of keys
# exit out of tmux
control + b + d

# scroll in tmux
control + b + [

